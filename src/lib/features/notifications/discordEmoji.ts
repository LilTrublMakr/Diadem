/** `<:name:id>` for a known emoji, or "" if not uploaded (renders as nothing, not broken text). */
export function discordEmojiTag(name: string): string {
	const id = DISCORD_EMOJI_IDS[name];
	return id ? `<:${name}:${id}>` : "";
}

/**
 * Pulls every `<a?:name:id>` reference out of rendered embed text. Discord falls back to
 * showing the raw `:name:`-style text (no icon) when an id doesn't resolve — usually because
 * it was uploaded as an Application Emoji under a different Discord Application than the one
 * whose bot token is actually sending the message, or the id was mistyped in DISCORD_EMOJI_IDS.
 */
export function extractEmojiTags(text: string): { name: string; id: string }[] {
	const matches = text.matchAll(/<a?:(\w+):(\d+)>/g);
	return [...matches].map((m) => ({ name: m[1], id: m[2] }));
}
